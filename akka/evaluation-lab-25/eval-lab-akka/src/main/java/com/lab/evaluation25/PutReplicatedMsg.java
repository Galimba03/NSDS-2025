package com.lab.evaluation25;

public class PutReplicatedMsg {

    private String name;
    private String email;

    public PutReplicatedMsg(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

}
